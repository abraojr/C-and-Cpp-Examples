#include <iostream>

using std::cout;
using std::endl;

int main()
{
    int lowercase_vowel = 'a';
    int uppercase_vowel = 'A';

    cout << "The lowercase vowel is: " << lowercase_vowel << endl;
    cout << "The uppercase vowel is: " << uppercase_vowel << endl;

    return 0;
}

/* OUTPUT
    The lowercase vowel is: 97
    The uppercase vowel is: 65
*/